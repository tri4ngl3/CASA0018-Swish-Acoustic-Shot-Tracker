import pandas as pd
import numpy as np
import seaborn as sns
import matplotlib.pyplot as plt
from sklearn.metrics import confusion_matrix, classification_report
from google.colab import auth
import gspread
from google.auth import default

auth.authenticate_user()
creds, _ = default()
gc = gspread.authorize(creds)

print("authenticated")

spreadsheet_name = "deployment_test"
sh = gc.open(spreadsheet_name)
worksheet = sh.get_worksheet(0)
raw_data = worksheet.get_all_values()

df = pd.DataFrame(raw_data[1:], columns=raw_data[0])
df['shot_outcome'] = df['shot_outcome'].str.strip().str.lower()
df['prediction'] = df['prediction'].str.strip().str.lower()

print(df.head())

from sklearn.metrics import confusion_matrix, f1_score
import matplotlib.colors as mcolors
import matplotlib.pyplot as plt
import numpy as np
import seaborn as sns

y_true = df['shot_outcome']
y_pred = df['prediction']
classes = ['make', 'miss', 'noise']

cm = confusion_matrix(y_true, y_pred, labels=classes)
cm_perc = cm.astype('float') / cm.sum(axis=1)[:, np.newaxis]
cm_perc = np.nan_to_num(cm_perc)
f1_scores = f1_score(y_true, y_pred, labels=classes, average=None)

fig, ax = plt.subplots(figsize=(9, 4.5))
fig.patch.set_facecolor('white')
ax.set_facecolor('white')

plot_data = np.vstack([cm_perc, np.zeros((1, 3))])
mask_diag = np.ones((4, 3), dtype=bool)
np.fill_diagonal(mask_diag[:3, :3], False)

mask_off_diag = np.zeros((4, 3), dtype=bool)
np.fill_diagonal(mask_off_diag[:3, :3], True)
mask_off_diag[3, :] = True

sns.heatmap(plot_data, mask=mask_diag, cmap='RdYlGn', vmin=0, vmax=1,
            annot=False, cbar=False, ax=ax, linewidths=1, linecolor='white')

sns.heatmap(plot_data, mask=mask_off_diag, cmap='Reds', vmin=0, vmax=1,
            annot=False, cbar=False, ax=ax, linewidths=1, linecolor='white')

for i in range(3):
    for j in range(3):
        val = cm_perc[i, j]
        val_str = f"{val*100:.1f}%" if val > 0 else "0%"
        if val_str.endswith(".0%"): val_str = val_str.replace(".0%", "%")
        ax.text(j + 0.5, i + 0.5, val_str, ha="center", va="center",
                color='black', fontsize=12, fontweight='bold' if i == j else 'normal')

for j in range(3):
    ax.text(j + 0.5, 3.5, f"{f1_scores[j]:.2f}", ha='center', va='center', color='black', fontsize=12)

ax.set_xticks(np.arange(3) + 0.5)
ax.set_yticks(np.arange(4) + 0.5)
display_classes = [c.title() for c in classes]
ax.set_xticklabels(display_classes, fontsize=12, color='black')
ax.set_yticklabels(display_classes + ['F1 Score'], fontsize=12, color='black', rotation=0)
ax.xaxis.tick_top()
ax.tick_params(axis='both', which='both', length=0)
plt.setp(ax.get_yticklabels(), ha="right")
ax.tick_params(axis='y', pad=10)

for _, spine in ax.spines.items():
    spine.set_visible(False)

plt.savefig('academic_confusion_matrix.png', dpi=300, bbox_inches='tight')
plt.show()

from sklearn.metrics import accuracy_score
overall_accuracy = accuracy_score(y_true, y_pred)

print(f"accuracy: {overall_accuracy * 100:.1f}%")